// 1. Intro: Why loops?
console.log("Without loops:");
console.log(1);
console.log(2);
console.log(3);
// ... tedious!

console.log("With a for loop:");
for (let i = 1; i <= 3; i++) {
  console.log(i);
}

// 2. While loop example
let count = 1;
while (count <= 3) {
  console.log("Count is:", count);
  count++;
}

// 3. Do...while loop example
let num = 1;
do {
  console.log("Number is:", num);
  num++;
} while (num <= 3);
